Drop Database if exists proyecto;
create database proyecto;
use proyecto;

-- Catalogo de tipos

CREATE TABLE ROL (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Tipo VARCHAR(50) NOT NULL
);

CREATE TABLE ESTADO (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(50) NOT NULL
);

CREATE TABLE MOTIVO (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(50) NOT NULL
);

CREATE TABLE TIPO_ACTIVIDAD (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Tipo VARCHAR(100) NOT NULL
);

CREATE TABLE SANGRE (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Tipo VARCHAR(10) NOT NULL
);

CREATE TABLE TIPO_DISCAPACIDAD (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(50) NOT NULL
);

CREATE TABLE CONDICION_MEDICA (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(100) NOT NULL
);

CREATE TABLE ALERGIA (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Tipo VARCHAR(100) NOT NULL
);

CREATE TABLE DISCAPACIDAD (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Tipo VARCHAR(50),
    TIPO_DISCAPACIDAD_Id INT,
    FOREIGN KEY (TIPO_DISCAPACIDAD_Id) REFERENCES TIPO_DISCAPACIDAD(Id)
);

-- Tablas Principales

CREATE TABLE CARRERAS (
    Codigo INT PRIMARY KEY,
    Nombre VARCHAR(50) NOT NULL,
    Coordinador VARCHAR(100)
);

CREATE TABLE ESTUDIANTE (
    Carne INT PRIMARY KEY,
    Cui VARCHAR(45),
    Nombres VARCHAR(50),
    Apellidos VARCHAR(50),
    Fecha_Nac DATETIME,
    Genero VARCHAR(20),
    Fotografia VARCHAR(100),
    Correo_Institucional VARCHAR(100),
    Correo_Personal VARCHAR(100),
    Direccion VARCHAR(150),
    Anio_Ingreso DATETIME,
    Inscrito TINYINT,
    PensumCerrado TINYINT,
    SANGRE_Id INT,
    FOREIGN KEY (SANGRE_Id) REFERENCES SANGRE(Id)
);

-- Relacion estudiante/Carrera

CREATE TABLE PERTENECE (
    ESTUDIANTE_Carne INT,
    CARRERAS_Codigo INT,
    PRIMARY KEY (ESTUDIANTE_Carne, CARRERAS_Codigo),
    FOREIGN KEY (ESTUDIANTE_Carne) REFERENCES ESTUDIANTE(Carne),
    FOREIGN KEY (CARRERAS_Codigo) REFERENCES CARRERAS(Codigo)
);



CREATE TABLE USUARIO (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(50),
    Contrasenia VARCHAR(100),
    ESTUDIANTE_Carne INT NULL,
    ROL_Id INT,
    FOREIGN KEY (ESTUDIANTE_Carne) REFERENCES ESTUDIANTE(Carne),
    FOREIGN KEY (ROL_Id) REFERENCES ROL(Id)
);



CREATE TABLE JUSTIFICACION (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Fecha_Presentacion DATETIME,
    Documento_Respaldo VARCHAR(200),
    Descripcion VARCHAR(200),
    ESTUDIANTE_Carne INT,
    MOTIVO_Id INT,
    ESTADO_Id INT,
    FOREIGN KEY (ESTUDIANTE_Carne) REFERENCES ESTUDIANTE(Carne),
    FOREIGN KEY (MOTIVO_Id) REFERENCES MOTIVO(Id),
    FOREIGN KEY (ESTADO_Id) REFERENCES ESTADO(Id)
);

CREATE TABLE FECHA_AUSENCIA (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Fecha_Ausencia DATETIME,
    JUSTIFICACION_Id INT,
    FOREIGN KEY (JUSTIFICACION_Id) REFERENCES JUSTIFICACION(Id)
);



CREATE TABLE NOTIFICACION (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Mensaje VARCHAR(200),
    Leido TINYINT,
    Fecha DATETIME,
    USUARIO_Id INT,
    JUSTIFICACION_Id INT,
    FOREIGN KEY (USUARIO_Id) REFERENCES USUARIO(Id),
    FOREIGN KEY (JUSTIFICACION_Id) REFERENCES JUSTIFICACION(Id)
);



CREATE TABLE ACTIVIDAD_ACADEMICA (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Nombre VARCHAR(100),
    Organizacion VARCHAR(100),
    Fecha_Inicio DATETIME,
    Fecha_Fin DATETIME,
    Observacion VARCHAR(200),
    ESTUDIANTE_Carne INT,
    TIPO_ACTIVIDAD_Id INT,
    FOREIGN KEY (ESTUDIANTE_Carne) REFERENCES ESTUDIANTE(Carne),
    FOREIGN KEY (TIPO_ACTIVIDAD_Id) REFERENCES TIPO_ACTIVIDAD(Id)
);



CREATE TABLE CONTACTO (
    DPI INT PRIMARY KEY,
    Nombre VARCHAR(100),
    Parentesco VARCHAR(50),
    Direccion VARCHAR(150),
    ESTUDIANTE_Carne INT,
    FOREIGN KEY (ESTUDIANTE_Carne) REFERENCES ESTUDIANTE(Carne)
);



CREATE TABLE TELEFONOS_ESTUDIANTE (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Telefono VARCHAR(50),
    ESTUDIANTE_Carne INT,
    FOREIGN KEY (ESTUDIANTE_Carne) REFERENCES ESTUDIANTE(Carne)
);

CREATE TABLE TELEFONOS_CONTACTO (
    Id INT AUTO_INCREMENT PRIMARY KEY,
    Telefono VARCHAR(50),
    CONTACTO_DPI INT,
    FOREIGN KEY (CONTACTO_DPI) REFERENCES CONTACTO(DPI)
);



CREATE TABLE ESTUDIANTE_CONDICION_MEDICA (
    ESTUDIANTE_Carne INT,
    CONDICION_MEDICA_Id INT,
    PRIMARY KEY (ESTUDIANTE_Carne, CONDICION_MEDICA_Id),
    FOREIGN KEY (ESTUDIANTE_Carne) REFERENCES ESTUDIANTE(Carne),
    FOREIGN KEY (CONDICION_MEDICA_Id) REFERENCES CONDICION_MEDICA(Id)
);

CREATE TABLE ALERGIA_has_ESTUDIANTE (
    ESTUDIANTE_Carne INT,
    ALERGIA_Id INT,
    PRIMARY KEY (ESTUDIANTE_Carne, ALERGIA_Id),
    FOREIGN KEY (ESTUDIANTE_Carne) REFERENCES ESTUDIANTE(Carne),
    FOREIGN KEY (ALERGIA_Id) REFERENCES ALERGIA(Id)
);

CREATE TABLE ESTUDIANTE_has_DISCAPACIDAD (
    ESTUDIANTE_Carne INT,
    DISCAPACIDAD_Id INT,
    PRIMARY KEY (ESTUDIANTE_Carne, DISCAPACIDAD_Id),
    FOREIGN KEY (ESTUDIANTE_Carne) REFERENCES ESTUDIANTE(Carne),
    FOREIGN KEY (DISCAPACIDAD_Id) REFERENCES DISCAPACIDAD(Id)
);




